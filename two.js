// Create a length converter function

// convert millimeter to meter
function milliMeter(mm){
    return mm / 1000 
}
let meter = milliMeter(4000)
console.log(meter + 'meter')

// convert kilogram to gram
function kiloGram(Kg){
    return Kg / 1000
}
let gram = kiloGram(9000)
console.log(gram + 'gram')